    private void fillToTable() {
        model.setColumnCount(0);
        for (student sv : lstSV) {
            model.addRow(new Object[]{sv.getId(), sv.getName(), sv.getGender(), sv.getMarkAVG(), sv.getStatus(sv.getMarkAVG())});
        }
    }

    private void showDetail() {
        sv = lstSV.get(index);
        txtID.setText(sv.getId());
        txtName.setText(sv.getName());
        txtMark.setText(sv.getMarkAVG() + "");
        cboGender.setSelectedItem(sv.getGender());
        tblSV.setRowSelectionInterval(index, index);
    }

    private void clear() {
        txtID.setText("");
        txtMark.setText("");
        txtName.setText("");
        cboGender.setSelectedIndex(0);
    }

    private void add() {
        String name, id, gender;
        id = txtID.getText();
        name = txtName.getText();
        gender = (String) cboGender.getSelectedItem();
        double mark = Double.parseDouble(txtMark.getText());
        student sv = new student(id, name, gender, mark);
        model.addRow(new Object[]{sv.getId(), sv.getName(), sv.getGender(), sv.getMarkAVG(), sv.getStatus(sv.getMarkAVG())});
        index = lstSV.size() - 1;
        fillToTable();
        showDetail();

    }

