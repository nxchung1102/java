    private void fillToTable() {
        model.setRowCount(0);
        for (student sv : lstSV) {
            model.addRow(new Object[]{sv.getId(), sv.getName(), sv.getGender(), sv.getMarkAVG(), sv.getStatus(sv.getMarkAVG())});
        }
    }

    private void showDetail() {
        student sv = lstSV.get(index);
        txtid.setText(sv.getId());
        txtname.setText(sv.getName());
        if (lstSV.get(index).getGender().equalsIgnoreCase("nam")) {
            rdomale.setSelected(true);
        } else {
            rdofemale.setSelected(true);
        }
        txtmark.setText(sv.getMarkAVG() + "");
        tblsv.setRowSelectionInterval(index, index);
    }

    private void clear() {
        txtid.setText("");
        txtname.setText("");
        txtmark.setText("");
    }

    private void add() {

        String id = txtid.getText();
        String name = txtname.getText();
        rdomale.setActionCommand("male");
        rdofemale.setActionCommand("female");
        String gender = btnRadioGroup.getSelection().getActionCommand();
        if (gender.equalsIgnoreCase("male")) {
            gender = "nam";
            rdomale.setSelected(true);
        } else {
            gender = "nữ";
            rdofemale.setSelected(true);
        }
        double mark = Double.parseDouble(txtmark.getText());
        lstSV.add(new student(id, name, gender, mark));
        fillToTable();
        index = lstSV.size() - 1;
    }

    private void wri() {
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("a.txt"));
            oos.writeObject(lstSV);
            oos.flush();
            oos.close();
            JOptionPane.showMessageDialog(this, "ghi file thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "loi ghi file");
        }

    }

    private void del() {
        int del = JOptionPane.showConfirmDialog(this, "co chac muon xoa", "canh bao", JOptionPane.YES_OPTION);
        if (del == JOptionPane.YES_OPTION) {
            if(tblsv.getSelectedRow()==-1){
            index = lstSV.size()-1;
            }else{
            index = tblsv.getSelectedRow();
            }
            lstSV.remove(index);
            fillToTable();
        }
    }
