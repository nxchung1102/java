    private void fillToTable() {
        model.setRowCount(0);
        for (SinhVien sv : lstsv) {
            model.addRow(new Object[]{sv.getHoTen(), sv.getNganh(), sv.getKy(), sv.getTrangThai(sv.getKy())});
        }
    }

    private void showDetail() {
        SinhVien sv = lstsv.get(index);
        txtName.setText(sv.getHoTen());
        cboNganh.setSelectedItem(sv.getNganh());
        txtKy.setText(sv.getKy() + "");
        tblsv.setRowSelectionInterval(index, index);
    }

    private void clear() {
        txtName.setText("");
        cboNganh.setSelectedIndex(0);
        txtKy.setText("");
    }

    private void add() {
        String name = txtName.getText();
        if (checkNull() && checkNum()) {
            String job = (String) cboNganh.getSelectedItem();
            int ky = Integer.parseInt(txtKy.getText());
            lstsv.add(new SinhVien(name, job, ky));
            fillToTable();
            index = lstsv.size() - 1;
            showDetail();
        }
    }

    private void del() {
        int chon = JOptionPane.showConfirmDialog(this, "ban co chac muon xoa", "canh bao", JOptionPane.YES_OPTION);
        if (chon == JOptionPane.YES_OPTION) {
            lstsv.remove(index);
            fillToTable();
            index = lstsv.size() - 1;
            clear();
        }
    }

    private void update() {
        if (checkNull() && checkNum()) {
            String name = txtName.getText();
            String job = (String) cboNganh.getSelectedItem();
            int ky = Integer.parseInt(txtKy.getText());
            SinhVien sv = new SinhVien(name, job, ky);
            lstsv.set(index, sv);
            model.setValueAt(name, index, 0);
            model.setValueAt(job, index, 1);
            model.setValueAt(ky, index, 2);
        }
    }

    private void wri() {
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("a.txt"));
            oos.writeObject(lstsv);
            oos.flush();
            oos.close();
            JOptionPane.showMessageDialog(this, "ghi thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ghi that bai");
        }

    }

    private void read() {
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream("a.txt"));
            lstsv = (List<SinhVien>) ois.readObject();
            ois.close();
            fillToTable();
            index = 0;
            showDetail();
            JOptionPane.showMessageDialog(this, "doc thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ghi that bai");
        }

    }

    public boolean checkNull() {
        if (txtName.getText().equalsIgnoreCase("") || txtKy.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "khong duoc de trong");
            return false;
        }
        return true;
    }

    public boolean checkNum() {
        try {
            Integer.parseInt(txtKy.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ky phai la so");
            return false;
        }
        if (Integer.parseInt(txtKy.getText()) < 1 || Integer.parseInt(txtKy.getText()) > 7) {
            JOptionPane.showMessageDialog(this, "ky phai tu 1-7");
            return false;
        }
        return true;
    }
