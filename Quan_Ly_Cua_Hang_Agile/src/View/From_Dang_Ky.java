    public boolean xacNhanMK() {
        String mk2 = txtXacNhanMK.getText().trim();
        if (mk2.equals(txtMatKhau.getText().trim())) {
            return true;
        }
        JOptionPane.showMessageDialog(this, "Xác nhận mật khẩu không khớp");
        return false;
    }

    public boolean checkNull() {
        String email = "^([a-z0-9_\\.-]+)@([\\da-z\\.-]+)\\.([a-z\\.]{2,6})$";
        if (txtTaiKhoan.getText().equals("") || txtMatKhau.getText().equals("") || txtXacNhanMK.getText().equals("") || txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trắng thông tin");
            return false;
        }
        String tk = txtTaiKhoan.getText();
        char kyTu;
        for (int i = 0; i < tk.length(); i++) {
            kyTu = tk.charAt(i);

            if (Character.isSpace(kyTu)) {
                JOptionPane.showMessageDialog(this, "Tài khoản không được có giấu cách !");
                return false;
            }
        }
        Pattern p = Pattern.compile("[^a-z0-9 ]", Pattern.CASE_INSENSITIVE);
        Matcher m = p.matcher(txtTaiKhoan.getText());
        boolean b = m.find();
        if (b) {
            JOptionPane.showMessageDialog(this,"Tên đăng nhập không được chứa kí tự đặc biệt !");
            return false;
        }
        
        
        if (!(_chucNang.accTonTai(txtTaiKhoan.getText().trim()))) {
            return false;
        }

        if (!(_chucNang.gmailTonTai(txtEmail.getText().trim()))) {
            return false;
        }

        try {
            Matcher match = Pattern.compile(email).matcher(txtEmail.getText().trim());
            if (!match.matches()) {
                JOptionPane.showMessageDialog(this, "Email không đúng định dạng");
                return false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return true;
    }

    public void clean() {
        txtEmail.setText("");
        txtMatKhau.setText("");
        txtTaiKhoan.setText("");
        txtXacNhanMK.setText("");
    }

