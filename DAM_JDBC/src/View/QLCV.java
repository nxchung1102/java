    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        String ma = txtma.getText();
        String ten = txtname.getText();
        VMChucVu p = new VMChucVu( ma, ten);
        JOptionPane.showMessageDialog(this, svr.addNewPosition(p));
        clear();
        fillToTable(svr.getAllPosition());

    }//GEN-LAST:event_btnaddActionPerformed

    private void btnupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupActionPerformed
        // TODO add your handling code here:
        try {
            String ma = txtma.getText();
            String ten = txtname.getText();
            String id = svr.getAllPosition().get(index).getId();
            VMChucVu p = new VMChucVu(ma, ten);
            JOptionPane.showMessageDialog(this, svr.UpdatePosition(id, p));
            index = 0;
            clear();
            fillToTable(svr.getAllPosition());
            tblp.setRowSelectionInterval(index, index);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "chon chuc vu muon sua");
            return;
        }
    }//GEN-LAST:event_btnupActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        try {
            String id = svr.getAllPosition().get(index).getId();
            JOptionPane.showMessageDialog(this, svr.DeletePosition(id));
            index=0;
            clear();
            fillToTable(svr.getAllPosition());
            tblp.setRowSelectionInterval(index, index);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "chon chuc vu muon xoa");
            return;
        }
    }//GEN-LAST:event_btnxoaActionPerformed

