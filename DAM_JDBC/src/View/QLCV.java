    public void fillToTable(ArrayList<VMChucVu> lstvp) {
        model = (DefaultTableModel) tblp.getModel();
        model.setRowCount(0);
        for (VMChucVu p : lstvp) {
            model.addRow(new Object[]{p.getId(), p.getMa(), p.getTen()});
        }
    }

    public void clear() {
        txtid.setText("");
        txtma.setText("");
        txtname.setText("");
    }

    public void showIndex() {
        VMChucVu p = svr.getAllPosition().get(index);
        txtid.setText(p.getId());
        txtma.setText(p.getMa());
        txtname.setText(p.getTen());
        tblp.setRowSelectionInterval(index, index);
    }

