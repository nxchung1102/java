    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        String idgh = cboidgh.getSelectedItem() + "";
        String idctsp = cboidctsp.getSelectedItem() + "";
        int sl = Integer.parseInt(txtsl.getText());
        double dg = Double.parseDouble(txtdg.getText());
        double dgkg = Double.parseDouble(txtdgkg.getText());
        VMGioHangChiTiet g = new VMGioHangChiTiet(idgh, idctsp, sl, dg, dgkg);
        JOptionPane.showMessageDialog(this, svr.addNewGHCT(g));
        clear();
        fillToTable(svr.getAllGHCT());
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        try {
            String id = svr.getAllGHCT().get(index).getIdGioHang();
            int sl = Integer.parseInt(txtsl.getText());
            double dg = Double.parseDouble(txtdg.getText());
            double dgkg = Double.parseDouble(txtdgkg.getText());
            VMGioHangChiTiet g = new VMGioHangChiTiet(sl, dg, dgkg);
            JOptionPane.showMessageDialog(this, svr.UpdateGHCT(id, g));
            index = 0;
            clear();
            fillToTable(svr.getAllGHCT());
            tblqlghct.setRowSelectionInterval(index, index);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "chon HDCT muon sua");
            return;
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        try {
            String id1 = svr.getAllGHCT().get(index).getIdGioHang();
            String id2 = svr.getAllGHCT().get(index).getIdChiTietSP();
            JOptionPane.showMessageDialog(this, svr.DeleteGHCT(id1,id2));
            index = 0;
            clear();
            fillToTable(svr.getAllGHCT());
            tblqlghct.setRowSelectionInterval(index, index);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "chon HDCT muon xoa");
            return;
        }
    }//GEN-LAST:event_btnxoaActionPerformed

