    public void loadCBOCTSP(ArrayList<VMChiTietSP> lstctsp) {
        CBOModel = new DefaultComboBoxModel();
        CBOModel = (DefaultComboBoxModel) cboidctsp.getModel();
        for (VMChiTietSP sp : lstctsp) {
            CBOModel.addElement(sp.getId());
        }
    }

    public void loadCBOGH(ArrayList<VMGioHang> lstgh) {
        CBOModel = new DefaultComboBoxModel();
        CBOModel = (DefaultComboBoxModel) cboidgh.getModel();
        for (VMGioHang gh : lstgh) {
            CBOModel.addElement(gh.getId());
        }
    }

    public void fillToTable(ArrayList<VMGioHangChiTiet> lstghct) {
        model = (DefaultTableModel) tblqlghct.getModel();
        model.setRowCount(0);
        for (VMGioHangChiTiet ct : lstghct) {
            model.addRow(new Object[]{
                ct.getIdGioHang(),
                ct.getIdChiTietSP(),
                ct.getSoLuong(),
                ct.getDonGia(),
                ct.getDonGiaKhiGiam()
            });
        }
    }

    public void clear() {
        cboidgh.setSelectedIndex(0);
        cboidctsp.setSelectedIndex(0);
        txtsl.setText("");
        txtdg.setText("");
        txtdgkg.setText("");
    }

    public void showIndex() {
        VMGioHangChiTiet h = svr.getAllGHCT().get(index);
        cboidgh.setSelectedItem(h.getIdGioHang());
        cboidctsp.setSelectedItem(h.getIdChiTietSP());
        txtsl.setText(h.getSoLuong() + "");
        txtdg.setText(h.getDonGia() + "");
        txtdgkg.setText(h.getDonGiaKhiGiam() + "");
        tblqlghct.setRowSelectionInterval(index, index);
    }

