    private void fillToTable() {
        model.setRowCount(0);
        for (NhaCC n : lstCC) {
            model.addRow(new Object[]{n.getName(), n.getChuHo(), n.getGia(), n.getLoai()});
        }
    }

    private void showDetail() {
        NhaCC n = lstCC.get(index);
        txtname.setText(n.getName());
        txtchuho.setText(n.getChuHo());
        txtgia.setText(n.getGia() + "");
        cboloai.setSelectedItem(n.getLoai());
        tblCC.setRowSelectionInterval(index, index);
       
    }

    private void clear() {
        txtchuho.setText("");
        txtname.setText("");
        txtgia.setText("");
        cboloai.setSelectedIndex(0);
    }

    private void add() {
        if (checkNull() && checkNum()&&checkName()) {

            String name, chuho, loai;
            double gia = Double.parseDouble(txtgia.getText());
            name = txtname.getText();
            chuho = txtchuho.getText();
            loai = (String) cboloai.getSelectedItem();
            lstCC.add(new NhaCC(name, chuho, gia, loai));
            fillToTable();
        }
    }

    public boolean checkNull() {
        if (txtname.getText().equalsIgnoreCase("") && txtchuho.getText().equalsIgnoreCase("") && txtgia.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "khong duoc de trong");
            return false;
        }
        return true;
    }

    public boolean checkNum() {
        try {
            Double.parseDouble(txtgia.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "phai la so");
            return false;
        }
        if (Double.parseDouble(txtgia.getText()) <= 500) {
            JOptionPane.showMessageDialog(this, "gia phai lon hon 500");
            return false;
        }
        return true;
    }
    public boolean checkName(){
    
        for (int i = 0; i < lstCC.size()-1; i++) {
            if(lstCC.get(index).getName().equalsIgnoreCase(txtname.getText())){
                JOptionPane.showMessageDialog(this, "ten khong duoc trung");
            return  false;
            }
        }
        return true;
    }

    private void del() {
   int hoi = JOptionPane.showConfirmDialog(this, "ban co chac muon xoa","canh bao",JOptionPane.YES_OPTION);
    if(hoi == JOptionPane.YES_OPTION){
         if(tblCC.getSelectedRow()==-1){
        index = lstCC.size()-1;
        }else{
        index = tblCC.getSelectedRow();
        }
        lstCC.remove(index);
        fillToTable();
         index = lstCC.size() - 1;
        showDetail();
    }
    }

