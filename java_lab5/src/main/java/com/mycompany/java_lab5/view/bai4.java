    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String path = txtName.getText();
            String content = txtContent.getText();
            BufferedWriter bw = new BufferedWriter(new FileWriter(path));
            bw.write(content);
            bw.newLine();
            bw.close();
            JOptionPane.showMessageDialog(this, "ghi file thanh cong");
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "ghi file that bai");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String path = txtName.getText();
            BufferedReader br = new BufferedReader(new FileReader(path));
            while (true){
            String line = br.readLine();
            if(line ==null)
            {
            break;
            }
            else {
            txtContent.append(line);
            txtContent.append("\n");
            }
            }
            JOptionPane.showMessageDialog(this, "doc file thanh cong");
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "doc file that bai");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

