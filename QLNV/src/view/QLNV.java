    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        index = tblnv.getSelectedRow();
        try {
            String ma = QLSV.getAll().get(index).getMa();
            int check = JOptionPane.showConfirmDialog(this, "ban co chac muon xoa", "canh bao", JOptionPane.YES_OPTION);
            if (check == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, QLSV.del(ma));
                LoadData(QLSV.getAll());
            } else {
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "chon nhan vien muon xoa");
        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        index = tblnv.getSelectedRow();
        try {
            String ma = QLSV.getAll().get(index).getMa();
            String ho = txtho.getText();
            String td = txtdem.getText();
            String t = txtten.getText();
            String gt = txtgt.getText();
            NhanVien nv = new NhanVien(ho, td, t, gt);
            int check = JOptionPane.showConfirmDialog(this, "ban co chac muon sua", "canh bao", JOptionPane.YES_OPTION);
            if (check == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, QLSV.update(ma, nv));
                LoadData(QLSV.getAll());
            } else {
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "chon nhan vien muon sua");
        }
    }//GEN-LAST:event_btnsuaActionPerformed

