    public void LoadData(ArrayList<NhanVien> lstnv) {
        model = (DefaultTableModel) tblnv.getModel();
        model.setRowCount(0);
        for (NhanVien nv : lstnv) {
            model.addRow(new Object[]{
                nv.getHo(), nv.getTendem(), nv.getTen(), nv.getGioiTinh()
            });
        }
    }

    public void showDetail() {
        NhanVien nv = QLSV.getAll().get(index);
        txtho.setText(nv.getHo());
        txtdem.setText(nv.getTendem());
        txtten.setText(nv.getTen());
        txtgt.setText(nv.getGioiTinh());
        tblnv.setRowSelectionInterval(index, index);
    }

