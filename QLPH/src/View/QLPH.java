    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        if (valueDate()) {
            String name = txtname.getText();
            int tang, sosv, tt;
            tang = Integer.parseInt(txttang.getText());
            sosv = Integer.parseInt(txtsosv.getText());
            if (cbott.getSelectedItem().equals("Rảnh")) {
                tt = 1;
            } else {
                tt = 0;
            }
            PhongHoc ph = new PhongHoc(name, tang, sosv, tt);
            JOptionPane.showMessageDialog(this, QLPH.addNew(ph));
            loadData(QLPH.getAll());
        } else {
            return;
        }
    }//GEN-LAST:event_btnaddActionPerformed

