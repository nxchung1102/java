    public void loadData(ArrayList<PhongHoc> lstph) {
        model = (DefaultTableModel) tblph.getModel();
        model.setRowCount(0);
        for (PhongHoc p : lstph) {
            model.addRow(new Object[]{
                p.getId(), p.getTen(), p.getTang(), p.getSoSV(), p.getTT(p.getTrangThai())
            });
        }
    }

    public void showDetail() {
        PhongHoc p = QLPH.getAll().get(index);
        txtid.setText(p.getId());
        txtname.setText(p.getTen());
        txtsosv.setText(p.getSoSV() + "");
        txttang.setText(p.getTang() + "");
        cbott.setSelectedItem(p.getTT(p.getTrangThai()));
        tblph.setRowSelectionInterval(index, index);
    }

