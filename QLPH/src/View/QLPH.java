    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        index = tblph.getSelectedRow();
        try {
            PhongHoc p = QLPH.getAll().get(index);
            String ma = p.getId();
            String name = txtname.getText();
            int tang, sosv, tt;
            tang = Integer.parseInt(txttang.getText());
            sosv = Integer.parseInt(txtsosv.getText());
            if (cbott.getSelectedItem().equals("Rảnh")) {
                tt = 0;
            } else {
                tt = 1;
            }
            PhongHoc ph = new PhongHoc(name, tang, sosv, tt);
            JOptionPane.showMessageDialog(this, QLPH.up(ma, ph));
            loadData(QLPH.getAll());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "chon phong muon sua");
            return;
        }
    }//GEN-LAST:event_btnsuaActionPerformed

