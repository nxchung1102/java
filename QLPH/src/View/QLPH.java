public boolean valueDate() {
        if (c.checkNull(txtname, "ten khong duoc de trong")) {
            return false;
        }
        if (c.checkNull(txtsosv, "so sv khong duoc de trong")) {
            return false;
        }
        if (c.checkNull(txttang, "tang khong duoc de trong")) {
            return false;
        }
        if (c.checkNum(txtsosv, "so sv phai la so")) {
            return false;
        }
        if (c.checkNum(txttang, "tang phai la so")) {
            return false;
        }
        if (c.checkNumPlus(txtsosv, "so sv phai >0")) {
            return false;
        }
        if (c.doMain(txttang, "tang phai > 0 & < 10")) {
            return false;
        }
        if (QLPH.checkPhong(txtname.getText())) {
            JOptionPane.showMessageDialog(this, "khong duoc trung ten phong");
            return false;
        }
        return true;
    }
