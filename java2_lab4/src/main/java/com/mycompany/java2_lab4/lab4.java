    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed

        uhelper.CheckNull(txtName, "ten khong duoc de trong");
        uhelper.CheckNull(txtDate, "date khong duoc de trong");
        uhelper.CheckNull(txtSalary, "salary khong duoc de trong");
        try {
            XDate.checkDate(txtDate.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "date khong dung dinh dang");
        }
        try {

            double salary = Double.parseDouble(txtSalary.getText());
            if (salary <= 0) {
                txtSalary.setBackground(Color.YELLOW);
                JOptionPane.showMessageDialog(this, "vui long nhap luong > 0");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "vui long nhap luong la so");
            txtSalary.requestFocus();
            txtSalary.setBackground(Color.YELLOW);
            return;
        }

    }//GEN-LAST:event_btnCheckActionPerformed

