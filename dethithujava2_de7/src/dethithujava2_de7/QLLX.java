    private void fillToTable() {
        model.setRowCount(0);
        for (Laixe lx : lstLX) {
            model.addRow(new Object[]{lx.getTen(), lx.getLoaiBang(), lx.getNamKN(), lx.getTrangThai(lx.getNamKN())});
        }
    }

    private void showDetail() {
        Laixe lx = lstLX.get(index);
        txtname.setText(lx.getTen());
        cboBang.setSelectedItem(lx.getLoaiBang());
        txtNamKN.setText(lx.getNamKN() + "");
        tblLX.setRowSelectionInterval(index, index);
    }

    private void clear() {
        txtname.setText("");
        cboBang.setSelectedIndex(0);
        txtNamKN.setText("");
    }

    private void add() {
        if (checkNull() && checkNum() && CheckDomain()) {
            String name = txtname.getText();
            String loai = (String) cboBang.getSelectedItem();
            int nam = Integer.parseInt(txtNamKN.getText());
            lstLX.add(new Laixe(name, loai, nam));
            fillToTable();
            index = lstLX.size() - 1;
            showDetail();
        }
    }

    private void del() {
        int chon = JOptionPane.showConfirmDialog(this, "co chac muon xoa", "canh bao", JOptionPane.YES_OPTION);
        if (chon == JOptionPane.YES_OPTION) {
            index = tblLX.getSelectedRow();
            lstLX.remove(index);
            index = lstLX.size() - 1;
            fillToTable();
            clear();
        }
    }

    private void wri() {
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("a.txt"));
            oos.writeObject(lstLX);
            oos.flush();
            oos.close();
            JOptionPane.showMessageDialog(this, "ghi file thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "loi ghi file");
        }

    }

    private void read() {
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream("a.txt"));
            lstLX = (List<Laixe>) ois.readObject();
            ois.close();
            JOptionPane.showMessageDialog(this, "doc file thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "loi doc file");
        }

    }

    public boolean checkNull() {
        if (txtname.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "ten khong duoc de trong");
            return false;
        }
        return true;
    }

    public boolean checkNum() {
        try {
            Double.parseDouble(txtNamKN.getText());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "tuoi phai la so");
            return false;
        }
        return true;
    }

    public boolean CheckDomain() {
        if (Double.parseDouble(txtNamKN.getText()) <= 1 || Double.parseDouble(txtNamKN.getText()) >= 20) {
            JOptionPane.showMessageDialog(this, "nam KN phai >=1 va <=20");
            return false;
        }
        return true;
    }

    private void update() {

        if (checkNull() && checkNum() && CheckDomain()) {
            String name = txtname.getText();
            String loai = (String) cboBang.getSelectedItem();
            int nam = Integer.parseInt(txtNamKN.getText());
            Laixe lx = new Laixe(name, loai, nam);
            lstLX.set(index, lx);
            model.setValueAt(name, index, 0);
            model.setValueAt(loai, index, 1);
            model.setValueAt(nam, index, 2);
            showDetail();
        }
    }

